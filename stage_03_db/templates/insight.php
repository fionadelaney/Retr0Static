<h3>Big Stories on Small Screens</h3>
<p>
In the games we play, narrative structure is what makes us care about our character. It makes us want to achieve goals and 
save the day.
<br><br>

<img src="/images/game_books.jpg" alt="Games in book form" width="100%" height="auto">
<br><br>
The same is true for a good book, whether it’s a thriller, science fiction or fantasy novel; 
it’s the narrative, the characters and the pacing that bring us in.
<br><br>


The three act structure divides a story in three:
<br><br>
The beginning, where conflict is established,
The middle, where implications of the conflict evolve and
The resolution, where the protagonist saves the day (or dies).
<br><br>
<img src="/images/Little-Video-Game-Books-joebot-6.jpg" alt="Bioshock as a kids book cover pic" width="100%" height="auto">

<br><br>
In most games, the first act is often played out in cut scenes 
at the start of the game. Most video games center on the second act, where all the running around 
and shooting takes place.<br><br>

The third act is usually the final confrontation, often another cut scene. 
Some of the best narrative-led games stretch out these different acts in a subtle and surprising way.
